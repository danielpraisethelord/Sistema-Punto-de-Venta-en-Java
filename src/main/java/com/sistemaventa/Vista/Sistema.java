    private void tableProveedorMouseClicked(java.awt.event.MouseEvent evt) {
        int fila = tableProveedor.rowAtPoint(evt.getPoint());
        if (fila >= 0) { // Verificar que la fila sea válida
            Object valorIdProveedor = tableProveedor.getValueAt(fila, 0);
            Object valorRucProveedor = tableProveedor.getValueAt(fila, 1);
            Object valorNombreProveedor = tableProveedor.getValueAt(fila, 2);
            Object valorTelefonoProveedor = tableProveedor.getValueAt(fila, 3);
            Object valorDireccionProveedor = tableProveedor.getValueAt(fila, 4);
            Object valorRazonProveedor = tableProveedor.getValueAt(fila, 5);
    
            // Verificar si los valores son null antes de llamar a toString()
            txtIdProveedor.setText(valorIdProveedor != null ? valorIdProveedor.toString() : "");
            txtRucProveedor.setText(valorRucProveedor != null ? valorRucProveedor.toString() : "");
            txtNomreProveedor.setText(valorNombreProveedor != null ? valorNombreProveedor.toString() : "");
            txtTelefonoProveedor.setText(valorTelefonoProveedor != null ? valorTelefonoProveedor.toString() : "");
            txtDireccionProveedor.setText(valorDireccionProveedor != null ? valorDireccionProveedor.toString() : "");
            txtRazonProveedor.setText(valorRazonProveedor != null ? valorRazonProveedor.toString() : "");
        } else {
            // Si la fila no es válida, establecer los campos de texto vacíos
            txtIdProveedor.setText("");
            txtRucProveedor.setText("");
            txtNomreProveedor.setText("");
            txtTelefonoProveedor.setText("");
            txtDireccionProveedor.setText("");
            txtRazonProveedor.setText("");
        }
    }
    
                                               

    private void btnEliminarProveedorActionPerformed(java.awt.event.ActionEvent evt) {                                                     
        // TODO add your handling code here:
        if (!"".equals(txtIdProveedor.getText())){
                int pregunta = JOptionPane.showConfirmDialog(null,"Estas seguro de eliminar?");
                if (pregunta == 0){
                    int id = Integer.parseInt(txtIdProveedor.getText());
                    prDao.EliminarProveedor(id);
                    LimpiarTable();
                    ListarProveedor();
                    LimpiarProveedor();
                    cbxProveedorProducto.removeAllItems(); // Elimina todos los elementos actuales
                    prDao.ConsultarProveedor(cbxProveedorProducto); // Consulta y agrega los proveedores nuevamente
    
                } 
        }
    }                                                    

